# Program Penjumlahan Dua Bilangan 
# Meminta inputan dari user 
bil1 = input('Masukkan bilangan pertama: ') 
bil2 = input('Masukkan bilangan kedua: ')
bil3 = input('Masukan bilangan ketiga: ')
bil4 = input('Masukan bilangan keempat: ')
bil5 = input ('Masukan bilangan kelima: ')
# Menjumlahkan bilangan 
jumlah = float(bil1) + float(bil2) - float(bil3) / float(bil4) * float(bil5)
# Menampilkan jumlah 
print('Jumlah {0} + {1} - {2} / {3} * {4} adalah {5}'.format(bil1, bil2, bil3, bil4, bil5, jumlah))